#include<stdio.h>
int main(){
    int t;
    scanf("%d",&t);
    while(t--){
        int x,c=0;
        scanf("%d",&x);
        c=x/11111111111;
        x=x%11111111111;
        c=(c*10)+x/1111111111;
        x=x%1111111111;
        c=(c*10)+x/111111111;
        x=x%111111111;
        c=(c*10)+x/11111111;
        x=x%11111111;
        c=(c*10)+x/1111111;
        x=x%1111111;
        c=(c*10)+x/111111;
        x=x%111111;
        c=(c*10)+x/11111;
        x=x%11111;
        c=(c*10)+x/1111;
        x=x%1111;
        c=(c*10)+x/111;
        x=x%111;
        x=x%11;
        if(c>=11-x || x==0) printf("YES\n");
        else printf("NO\n");
    }
}